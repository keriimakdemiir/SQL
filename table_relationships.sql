--A many-to-many relationship occurs when multiple records in a table are associated with multiple records in another table. 
--For example, a many-to-many relationship exists between customers and products: customers can purchase various products, 
--and products can be purchased by many customers.


--one-to-many relationship in a database occurs when each record in Table A may have many linked records in Table B, 
--but each record in Table B may have only one corresponding record in Table A. 
--A one-to-many relationship in a database is the most common relational database design and is at the heart of good design.


--A one-to-one relationship is a link between the information in two tables, 
--where each record in each table only appears once. 
--For example, there might be a one-to-one relationship between employees and the cars they drive.

